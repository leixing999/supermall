<template>
  <div class="tab-bar-item" @click="btnClick">
    <div v-if="isActive" >
      <slot name="item-icon-active"></slot>
    </div>
    <div v-else>
      <slot name="item-icon"></slot>
    </div>
    <div :style="activedStyle"><slot name="item-text"></slot></div>
  </div>
</template>

<script>
export default {
  name: 'tabBarItem',
  props: {
    path: String,
    activeColor: {
      type: String,
      default: 'red',
    },
  },
  data() {
    return {
      //isActive: false,
    }
  },
  methods: {
    btnClick() {
      this.$router.replace(this.path)
    },
  },
  computed: {
    isActive() {
      return this.$route.path.indexOf(this.path) >= 0
    },
    activedStyle() {
      return this.isActive ? { color: this.activeColor } : {}
    },
  },
}
</script>

